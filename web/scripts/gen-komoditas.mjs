#!/usr/bin/env node
// Generates src/lib/komoditas.generated.ts from ai_engine/grading_configs/*.json
// so the frontend's commodity list can never drift from the configs the
// grading engine actually supports. Run via `npm run gen:komoditas`
// (also wired into predev/prebuild).

import { readdirSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";
import path from "node:path";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const CONFIGS_DIR = path.resolve(__dirname, "../../ai_engine/grading_configs");
const OUT_FILE = path.resolve(__dirname, "../src/lib/komoditas.generated.ts");

// Label & kelompok tidak ada di file config (isinya cuma ambang batas
// angka), jadi tetap perlu dipetakan manual di sini. Id yang belum
// terdaftar tetap ikut dipakai — cuma dapat label/kelompok hasil format
// otomatis dari nama filenya sampai ditambahkan ke bawah.
const LABELS = {
  tomato_sayur: { label: "Tomat Sayur", kelompok: "Tomat" },
  tomato_beef: { label: "Tomat Beef", kelompok: "Tomat" },
  tomato_ceri: { label: "Tomat Ceri", kelompok: "Tomat" },
  tomato_merah: { label: "Tomat Merah", kelompok: "Tomat" },
  chili_rawit: { label: "Cabai Rawit Merah", kelompok: "Cabai" },
  chili_merah_keriting: { label: "Cabai Merah Keriting", kelompok: "Cabai" },
  chili_merah_besar: { label: "Cabai Merah Besar", kelompok: "Cabai" },
  chili_hijau_besar: { label: "Cabai Hijau Besar", kelompok: "Cabai" },
  carrot_lokal: { label: "Wortel Lokal", kelompok: "Wortel" },
  carrot_impor: { label: "Wortel Impor", kelompok: "Wortel" },
  cucumber_lokal: { label: "Timun Lokal", kelompok: "Timun" },
  cucumber_baby: { label: "Timun Baby", kelompok: "Timun" },
};

function autoLabel(id) {
  return id
    .split("_")
    .map((w) => w[0].toUpperCase() + w.slice(1))
    .join(" ");
}

function autoKelompok(id) {
  const base = id.split("_")[0];
  return base[0].toUpperCase() + base.slice(1);
}

const ids = readdirSync(CONFIGS_DIR)
  .filter((f) => f.endsWith(".json"))
  .map((f) => f.replace(/\.json$/, ""))
  .sort((a, b) => {
    const ka = LABELS[a]?.kelompok ?? autoKelompok(a);
    const kb = LABELS[b]?.kelompok ?? autoKelompok(b);
    return ka === kb ? a.localeCompare(b) : ka.localeCompare(kb);
  });

const entries = ids.map((id) => {
  const known = LABELS[id];
  const label = known?.label ?? autoLabel(id);
  const kelompok = known?.kelompok ?? autoKelompok(id);
  return `  { id: ${JSON.stringify(id)}, label: ${JSON.stringify(label)}, kelompok: ${JSON.stringify(kelompok)} },`;
});

const output = `// AUTO-GENERATED by scripts/gen-komoditas.mjs — do NOT edit by hand.
// Source of truth: ai_engine/grading_configs/*.json (one entry per file).
// Regenerate: npm run gen:komoditas

export const KOMODITAS: { id: string; label: string; kelompok: string }[] = [
${entries.join("\n")}
];
`;

writeFileSync(OUT_FILE, output);
console.log(`gen-komoditas: wrote ${ids.length} entries to ${path.relative(process.cwd(), OUT_FILE)}`);
